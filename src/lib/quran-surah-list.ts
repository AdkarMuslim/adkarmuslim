/** كلمات إضافية (لاتيني / بدون أل التعريف) لأشهر السور */
const SURAH_EXTRA_KEYWORDS: Record<number, string> = {
  1: "فاتحه fatiha",
  2: "بقره baqarah baqara cow",
  3: "عمران imran",
  18: "كهف kahf cave",
  36: "يس yaseen yasin",
  55: "رحمن rahman",
  67: "ملك mulk",
  112: "ikhlas اخلاص توحيد",
  113: "فلق falak",
  114: "ناس nas mankind",
};

export function getQuranSurahSearchItems(): Array<{
  href: string;
  title: string;
  keywords: string;
}> {
  return QURAN_SURAH_ARABIC_NAMES.map((name, i) => {
    const n = i + 1;
    const extra = SURAH_EXTRA_KEYWORDS[n] ?? "";
    const short = name.replace(/^ال/, "").trim();
    return {
      href: `/quran/${n}`,
      title: `سورة ${name}`,
      keywords: `${name} ${short} ${n} سوره سورة رقم quran surah ${extra}`.trim(),
    };
  });
}
